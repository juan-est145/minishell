#include "../../include/minishell.h"
#include "../../libft/libft.h"

char	*initial_print(char **env)
{
	int		i;
	char	*prompt;
	char	*color_prompt;

	i = 0;
	prompt = ft_getenv(env, "LOGNAME=");
	prompt = ft_strjoin(prompt, "$ ");
	color_prompt = ft_strjoin(GREEN, prompt);
	free(prompt);
	prompt = ft_strjoin(color_prompt, RESET);
	free(color_prompt);
	while (i++ < 1500)
		printf("\n");
	print_swamp();
	return (prompt);
}

